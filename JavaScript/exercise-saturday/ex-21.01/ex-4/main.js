// 4. Write   a   JavaScript   conditional
// statement to find the sign of product of
// three numbers. Display an alert box with
// the specified sign.

let num1 = 5;
let num2 = -10;
let num3 = 4;

let sum = num1 + num2 + num3;
console.log(sum);

let sign;

if (sum < 0 ) {
  sign = "-";
  alert(`The sign is ${sign}`);
} else {
  sign = "+";
  alert(`The sign is ${sign}`);
}






